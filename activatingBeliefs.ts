// FUNCIONES UTILITARIAS
export function getActivatingBeliefsForFlower(flowerName: string): string[] {
    return activatingBeliefs[flowerName] || [];
}

export function getRandomActivatingBelief(flowerName: string): string {
    const beliefs = activatingBeliefs[flowerName];
    if (!beliefs || beliefs.length === 0) return "";
    return beliefs[Math.floor(Math.random() * beliefs.length)];
}

export function hasActivatingBeliefs(flowerName: string): boolean {
    return activatingBeliefs[flowerName] && activatingBeliefs[flowerName].length > 0;
}
